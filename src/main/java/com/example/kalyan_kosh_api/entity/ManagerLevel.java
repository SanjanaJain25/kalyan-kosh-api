package com.example.kalyan_kosh_api.entity;

/**
 * Manager Level enum to define the hierarchy level of management
 * - SAMBHAG: Sambhag Level Manager (संभाग प्रबंधक)
 * - DISTRICT: District Level Manager (जिला प्रबंधक) 
 * - BLOCK: Block Level Manager (ब्लॉक प्रबंधक)
 */
public enum ManagerLevel {
    SAMBHAG,
    DISTRICT, 
    BLOCK
}